#define GX3201_VA_COUNTER_3_INI     (void *)(GX3201_VA_COUNTER_BASE + 0xa8)

/* interrupt */
#define GX3201_VA_INTC    (IO_VA_BASE + GX3201_PA_INTC)
#define GX3201_VA_INTC_NINT31_00	(void *)(GX3201_VA_INTC + 0x00)
