#ifndef _GX3201_MAP_H_
#define _GX3201_MAP_H_

#define GX3201_PA_SDRAM		(0x00100000)
#define GX3201_PA_COUNTER	(0x0020A000)
#define GX3201_PA_INTC		(0x00500000)
#define GX3201_PA_GPIOA		(0x00305000)
#define GX3201_PA_GPIOB		(0x00305000)
#define GX3201_PA_GPIOC		(0x00305000)

#define IO_VA_BASE (0xA0000000)

#define GX3201_VA_SDRM    (IO_VA_BASE + GX3201_PA_SDRAM)

/* conter */
#define GX3201_VA_COUNTER_1		(void*)	(IO_VA_BASE + GX3201_PA_COUNTER)
#define GX3201_VA_COUNTER_1_STATUS	(void*) (GX3201_VA_COUNTER_1 + 0x00)
#define GX3201_VA_COUNTER_1_VALUE	(void*) (GX3201_VA_COUNTER_1 + 0x04)
#define GX3201_VA_COUNTER_1_CONTROL	(void*) (GX3201_VA_COUNTER_1 + 0x10)
#define GX3201_VA_COUNTER_1_CONFIG	(void*) (GX3201_VA_COUNTER_1 + 0x20)
#define GX3201_VA_COUNTER_1_PRE		(void*) (GX3201_VA_COUNTER_1 + 0x24)
#define GX3201_VA_COUNTER_1_INI		(void*) (GX3201_VA_COUNTER_1 + 0x28)
#define GX3201_COUNTER_1_INT_EN     (1<<1)
#define GX3201_COUNTER_1_EN         (1<<0)

/* interrupt */
#define GX3201_VA_INTC			(void*) (IO_VA_BASE + GX3201_PA_INTC)
#define GX3201_VA_INTC_NINT31_00	(void*) (GX3201_VA_INTC + 0x00)
#define GX3201_VA_INTC_NINT63_32	(void*) (GX3201_VA_INTC + 0x04)
#define GX3201_VA_INTC_NEN31_00		(void*) (GX3201_VA_INTC + 0x20)
#define GX3201_VA_INTC_NEN63_32		(void*) (GX3201_VA_INTC + 0x24)
#define GX3201_VA_INTC_NMASK31_00	(void*) (GX3201_VA_INTC + 0x50)
#define GX3201_VA_INTC_NMASK63_32	(void*) (GX3201_VA_INTC + 0x54)
#define GX3201_VA_INTC_SOURCE		(void*) (GX3201_VA_INTC + 0x60)

/* gpio */
#define GX3201_VA_GPIOA    (void*) (IO_VA_BASE + GX3201_PA_GPIOA)
#define GX3201_VA_GPIOB    (void*) (IO_VA_BASE + GX3201_PA_GPIOB)
#define GX3201_VA_GPIOC    (void*) (IO_VA_BASE + GX3201_PA_GPIOC)

#endif /* _GX3201_MAP_H_ */

